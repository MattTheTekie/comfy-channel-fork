# Function to download and stream video, monitoring file size
download_and_stream() {
        cd /root/comfy-channel/temp_videos
        yt-dlp $url -f 'mp4[height<=?480][fps<=?25]+bestaudio/best'
	for file in *.mp4; do new_name=$(echo "$file" | sed "s/[^[:alnum:] ]//g" | sed 's/\.\.\.//g' | sed 's/\.\./?/g' | sed 's/mp4$//i'); mv "$file" "$new_name.mp4"; done
}

# Loop through each video URL
for url in "${input_urls[@]}"; do
    # Download and stream the video
    download_and_stream "$url"
